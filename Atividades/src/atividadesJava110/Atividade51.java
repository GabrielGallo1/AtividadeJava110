package atividadesJava110;

import java.util.Scanner;
public class Atividade51 {

	public class ExibirNumerosDoWhile {
	    public static void main(String[] args) {
	        int numero = 1;
	        do {
	            System.out.println(numero);
	            numero++;
	        } while (numero <= 10);
	    }
	}

}
