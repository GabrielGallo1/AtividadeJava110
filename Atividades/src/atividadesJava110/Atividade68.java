package atividadesJava110;

import java.util.Scanner;
public class Atividade68 {

	public class NumerosParesFor {
	    public static void main(String[] args) {
	        for (int numero = 1; numero <= 50; numero++) {
	            if (numero % 2 == 0) {
	                System.out.println(numero);
	            }
	        }
	    }
	}

}
